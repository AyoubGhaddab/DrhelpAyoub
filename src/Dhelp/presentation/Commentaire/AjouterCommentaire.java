    private void tb_loadSujetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_loadSujetActionPerformed
        jp_tbsujet.setVisible(true);
        String nameCat = cb_cat.getSelectedItem().toString();
         AddCommentDAO ad = new AddCommentDAO();
       ArrayList<Sujets> lp = new ArrayList<>();
       try{
       for(Object em:(ArrayList)ad.AfficherSujet(nameCat))
           lp.add((Sujets)em);
       }catch(SQLException ex){}
       
    DefaultTableModel amod = new DefaultTableModel();
    Object[] tableColumnNames = new Object[6];
    tableColumnNames[0]="titre sujet ";
    tableColumnNames[1]="sujet "; 
    tableColumnNames[2]="date de creation";
    tableColumnNames[3]="id";
    tableColumnNames[4]="Auteur";
    tableColumnNames[5]="temps de creation";
   
       
    amod.setColumnIdentifiers(tableColumnNames);
    Object[] objects = new Object[6];
    if(lp.size()>0)
    {
        for(int i=0;i<lp.size();i++)
        {
            Sujets hashmap = lp.get(i);
            objects[0]=hashmap.getNomSujet().toString();
            objects[1]=hashmap.getContenuSujet().toString();
            objects[2]=hashmap.getDateCrSujet().toString();
            objects[3]=hashmap.getIdSujet();
            objects[4]=hashmap.getAuteurSujet().toString();
            objects[5]=hashmap.getTempsCrSujet().toString();
            
            amod.addRow(objects);
        }
        this.table_s.setModel(amod);
        
    }
    else{
        JOptionPane.showMessageDialog(null,"il nexiste pas encore des sujets dans cette categories");
    }

    }//GEN-LAST:event_tb_loadSujetActionPerformed

