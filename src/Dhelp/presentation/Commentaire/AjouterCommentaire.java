    private void table_sMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_sMouseClicked
        jp_sujet.setVisible(true);
        AddCommentDAO ad = new AddCommentDAO();
        
                
        try{
            
            int row = table_s.getSelectedRow();
            String nomSujet =(table_s.getModel().getValueAt(row, 0).toString());
            String contenuSujet =(table_s.getModel().getValueAt(row, 1).toString());
            String DateCreSujet =(table_s.getModel().getValueAt(row, 2).toString());
            int idSujet = ((int)table_s.getModel().getValueAt(row, 3));
            String Auteursujet =(table_s.getModel().getValueAt(row, 4).toString());
            String tempCrSujet = (table_s.getModel().getValueAt(row, 5).toString());
            
            ta_contenu.setText(contenuSujet);
            lb_nomSujet.setText(nomSujet);
            //public Sujets(String nomSujet, String ContenuSujet, String dateCrSujet, int idSujet )
            
            Sujets s =  new Sujets(nomSujet, contenuSujet, DateCreSujet, idSujet, Auteursujet,tempCrSujet);
            int nc =ad.AfficherNbrCmnt(s.getIdSujet());
            loadsujet = s;
            lb_nbrcomm.setText(""+nc);
            if(ptdr.getLogin_personnes().equals(Auteursujet))
            {
                bu_update.setVisible(true);
                bu_delete.setVisible(true);
            }
            else
            {
                bu_update.setVisible(false);
                bu_delete.setVisible(false);
            }
        
        
        }
        catch(Exception e){JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_table_sMouseClicked

