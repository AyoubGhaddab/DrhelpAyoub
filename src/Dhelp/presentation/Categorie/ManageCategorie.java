    private void bu_loadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_loadActionPerformed
        AddCommentDAO cat = new AddCommentDAO();
       ArrayList<Categorie> lp = new ArrayList<>();
        try {
            for(Object em:(ArrayList)cat.GetNomCat())
                lp.add((Categorie)em);
        } catch (SQLException ex) {
           
        }

    DefaultTableModel amod = new DefaultTableModel();
    Object[] tableColumnNames = new Object[2];
    tableColumnNames[0]="id ";
    tableColumnNames[1]="Nom de la categorie "; 
    
       
    amod.setColumnIdentifiers(tableColumnNames);
    Object[] objects = new Object[2];
    if(lp.size()>0)
    {
        for(int i=0;i<lp.size();i++)
        {
            Categorie hashmap = lp.get(i);
            //objects[0]=hashmap.getNomSujet().toString();
            objects[0]=hashmap.getId_cat();
            objects[1]=hashmap.getNom_cat().toString();
            
            amod.addRow(objects);
        }
        this.tables.setModel(amod);
        
    }
    else{
        JOptionPane.showMessageDialog(null,"il nexiste pas encore des categorie ");
    }

    }//GEN-LAST:event_bu_loadActionPerformed

