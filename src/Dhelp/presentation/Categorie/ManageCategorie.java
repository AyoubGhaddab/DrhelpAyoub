    private void bu_addnewCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bu_addnewCatActionPerformed
       CategorieDAO cat = new CategorieDAO();
       if (tb_nomcat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "le champs  ne doit pas etre vide");
            
        }
        else if(tb_nomcat.getText().length()<5){
            JOptionPane.showMessageDialog(null, "veuileé saisir au moin 5 caractéres");
        }
        else {
            try {
               int row = tables.getSelectedRow();
            int idcat =((int)tables.getModel().getValueAt(row, 0));
            Categorie c = new Categorie(tb_nomcat.getText());
            cat.updateCategorie(c, idcat);
            JOptionPane.showMessageDialog(null, "la modification a ete enregistrer avec succes");
            bu_loadActionPerformed(null);
           } catch (Exception e) { JOptionPane.showMessageDialog(null,"erreur lors de linsertion "+e);
           }
            
        }
    }//GEN-LAST:event_bu_addnewCatActionPerformed

