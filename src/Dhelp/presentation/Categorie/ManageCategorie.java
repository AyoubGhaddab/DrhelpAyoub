    private void tb_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_addActionPerformed
        CategorieDAO cat = new CategorieDAO();
        
       if (tb_newnomCat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "le champs  ne doit pas etre vide");
            
        }
        else if(tb_newnomCat.getText().length()<5){
            JOptionPane.showMessageDialog(null, "veuileé saisir au moin 5 caractéres");
        }
        else {
            try {
                Categorie c = new Categorie(tb_newnomCat.getText());
                if(cat.findCategoriebyNom(tb_newnomCat.getText())){
                    JOptionPane.showMessageDialog(null, "le nom de cette categorie existe deja , veullié choisir un autre !");
                }
                else {
               cat.insertCategorie(c);
            JOptionPane.showMessageDialog(null, "Une nouvelle categorie vient detre creer");
            bu_loadActionPerformed(null);
                }
           } catch (Exception e) { JOptionPane.showMessageDialog(null,"erreur lors de linsertion "+e);
           }
            
        }
    }//GEN-LAST:event_tb_addActionPerformed

